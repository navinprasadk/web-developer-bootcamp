// attr()
$("#inputField").attr("placeholder", "type your name")
//  It will select the id named "inputField" and assigns the value to the placeholder attribute

// val()
$("#cityField").val()
// fetched the value of id named "cityField"

// text()
$("ul").text()
// fetches the text from the tag named "ul"

// html()
$("ul").html()
// fetches the html markup from the tag named "ul"

// addClass()
$("li:first-of-type").addClass("markup");
// add the class named "markup" to the first list

// removeClass()
$("li").removeClass("styling");
// remove the class named "styling" in the li tag

// toggleClass()
$("li:last").toggleClass("toggleGame");
// toggle the class named "toggleGame"
